        // Toggle de visualização
        const viewBtns = document.querySelectorAll('.view-btn');
        const timelineSections = document.querySelectorAll('.timeline-section');
        
        viewBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                viewBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                
                const view = btn.dataset.view;
                timelineSections.forEach(section => {
                    section.classList.remove('active');
                    if (section.id === 'timeline-' + view) {
                        section.classList.add('active');
                    }
                });
            });
        });
        
        // Filtro por tipo
        const statCards = document.querySelectorAll('.stat-card');
        const timelineItems = document.querySelectorAll('.timeline-item-enhanced');
        const releaseCards = document.querySelectorAll('.release-card');
        
        statCards.forEach(card => {
            card.addEventListener('click', () => {
                statCards.forEach(c => c.classList.remove('active'));
                card.classList.add('active');
                
                const filter = card.dataset.filter;
                
                // Filtrar timeline histórica
                timelineItems.forEach(item => {
                    if (filter === 'all' || item.dataset.tipo === filter) {
                        item.classList.remove('hidden');
                    } else {
                        item.classList.add('hidden');
                    }
                });
                
                // Filtrar timeline de lançamentos
                releaseCards.forEach(card => {
                    if (filter === 'all' || card.dataset.tipo === filter) {
                        card.classList.remove('hidden');
                    } else {
                        card.classList.add('hidden');
                    }
                });
            });
        });
        
        // Smooth scroll
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
                }
            });
        });
