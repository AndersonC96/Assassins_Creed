    <script>
        // Navegação por categoria
        const navBtns = document.querySelectorAll('.media-nav-btn');
        const sections = document.querySelectorAll('.media-section');
        
        navBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                // Update active button
                navBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                
                const target = btn.dataset.target;
                
                // Show/hide sections
                sections.forEach(section => {
                    if (target === 'all' || section.dataset.category === target) {
                        section.classList.remove('hidden');
                    } else {
                        section.classList.add('hidden');
                    }
                });
            });
        });
        
        // Busca
        const searchInput = document.getElementById('searchInput');
        const allCards = document.querySelectorAll('.media-card');
        
        searchInput.addEventListener('input', function() {
            const query = this.value.toLowerCase().trim();
            
            allCards.forEach(card => {
                const searchable = card.dataset.searchable || '';
                if (query === '' || searchable.includes(query)) {
                    card.style.display = '';
                } else {
                    card.style.display = 'none';
                }
            });
            
            // Show all sections when searching
            if (query !== '') {
                navBtns.forEach(b => b.classList.remove('active'));
                navBtns[0].classList.add('active');
                sections.forEach(section => section.classList.remove('hidden'));
            }
        });
        
        // Smooth scroll to anchors
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
                }
            });
        });
    </script>
